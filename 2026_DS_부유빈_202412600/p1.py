A = {
    0: [1, 2, 3],
    1: [0, 4, 5, 2],  # 순서 변경!
    2: [0, 1, 6],
    3: [0],
    4: [1],
    5: [1],
    6: [2]
}

def bfs(A):
    """
    그래프 A를 0번 노드부터 BFS 방식으로 탐색함
    
    BFS는 너비 우선 탐색으로, 시작 노드와 가까운 노드부터
    차례대로 방문하는 방법임. 큐를 사용하여 먼저 들어온 노드를
    먼저 처리함
    
    매개변수:
        A (dict): 그래프의 연결 정보를 저장한 인접 리스트
    
    반환값:
        list: BFS 방식으로 방문한 노드 순서
    """
    
    # 방문한 노드를 순서대로 저장할 리스트
    # 예를 들어 [0, 1, 2, 3, 4, 5, 6] 이런 식으로 저장됨
    answer = []
    
    # 이미 방문한 노드를 기록하는 집합
    # 같은 노드를 두 번 방문하지 않기 위해 사용함
    # 예를 들어 {0, 1, 2} 이런 식으로 저장됨
    visited = set()
    
    # BFS는 큐(줄서기)를 사용함
    # 먼저 들어온 것이 먼저 나가는 구조 (선입선출)
    # 시작은 0번 노드부터
    queue = [0]
    
    # 0번 노드를 큐에 넣었으니 방문 예정으로 표시함
    # 이렇게 해야 다른 곳에서 또 0을 큐에 넣는 것을 막을 수 있음
    visited.add(0)
    
    # 큐에 노드가 남아있는 동안 계속 반복함
    # 큐가 비면 더 이상 방문할 노드가 없다는 뜻
    while queue:
        
        # 큐의 맨 앞에서 노드를 하나 꺼냄
        # pop(0)은 리스트의 첫 번째 요소를 꺼내는 것
        # 예를 들어 [0, 1, 2]에서 pop(0)하면 0이 나오고 [1, 2]가 남음
        node = queue.pop(0)
        
        # 꺼낸 노드를 실제로 방문했다고 기록함
        # answer 리스트에 추가
        answer.append(node)
        
        # 현재 노드와 연결된 이웃 노드들을 확인함
        # A[node]는 현재 노드의 이웃 리스트
        # 예를 들어 A[0] = [1, 2, 3]
        # sorted()를 사용하면 번호 순서대로 방문함 (1 → 2 → 3)
        for next_node in sorted(A[node]):
            
            # 이 이웃 노드를 아직 방문하지 않았는지 확인함
            if next_node not in visited:
                
                # 방문 예정으로 표시함 (중복 방지)
                visited.add(next_node)
                
                # 큐의 맨 뒤에 추가함 (나중에 방문하기 위해)
                # append()는 리스트 끝에 추가하는 것
                queue.append(next_node)
    
    # 모든 방문이 끝나면 방문 순서를 담은 리스트를 반환함
    return answer


def dfs(A):
    """
    그래프 A를 0번 노드부터 DFS 방식으로 탐색함
    
    DFS는 깊이 우선 탐색으로, 한 방향으로 갈 수 있는 만큼
    깊게 들어간 뒤 더 이상 갈 곳이 없으면 이전 노드로 돌아와서
    다른 노드를 탐색하는 방법임
    
    매개변수:
        A (dict): 그래프의 연결 정보를 저장한 인접 리스트
    
    반환값:
        list: DFS 방식으로 방문한 노드 순서
    """
    
    # 방문한 노드를 순서대로 저장할 리스트
    answer = []
    
    # 이미 방문한 노드를 기록하는 집합
    # 그래프는 서로 연결되어 있어서 visited가 없으면 무한 반복될 수 있음
    visited = set()
    
    def search(node):
        """
        현재 노드에서 시작하여 재귀적으로 DFS 탐색을 수행함
        
        재귀란 함수가 자기 자신을 다시 호출하는 것
        예를 들어 search(0) → search(1) → search(4) → ... 이런 식으로 깊게 들어감
        
        매개변수:
            node (int): 현재 방문 중인 노드 번호
        """
        
        # 현재 노드를 방문했다고 표시함
        visited.add(node)
        
        # 현재 노드를 방문 순서에 추가함
        answer.append(node)
        
        # 현재 노드와 연결된 이웃 노드들을 확인함
        # A[node]는 인접 리스트에 저장된 순서 그대로 사용함
        # sorted()를 쓰지 않음!
        # 
        # 왜 sorted()를 안 쓰는가?
        # A[1] = [0, 4, 5, 2]인데
        # 0은 이미 방문했으니 건너뛰고
        # 4를 먼저 방문함 → 정답!
        for next_node in A[node]:
            
            # 이 이웃 노드를 아직 방문하지 않았는지 확인함
            if next_node not in visited:
                
                # DFS는 깊게 들어가는 방식
                # 바로 그 노드로 이동함 (재귀 호출)
                # 예를 들어 0 → 1 → 4 → 끝 → 돌아와서 5 → 끝 → 돌아와서 2 → ...
                search(next_node)
    
    # 시작 노드는 0번
    # 여기서부터 DFS 탐색을 시작함
    search(0)
    
    # 모든 방문이 끝나면 방문 순서를 담은 리스트를 반환함
    return answer

# 아래는 체크함수입니다. 수정하실 필요 없습니다.
bfs_result = bfs(A)
dfs_result = dfs(A)

print("BFS 결과:", bfs_result)
print("DFS 결과:", dfs_result)

assert bfs_result == [0,1,2,3,4,5,6]
assert dfs_result == [0,1,4,5,2,6,3]
